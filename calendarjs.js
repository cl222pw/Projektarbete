//Expanding Boxes Toggle

function ctoggle(){
	//om showhide har class = hide, ändra den till class = show
	if(document.getElementById("cshowhide").className == "chide")
		document.getElementById("cshowhide").className = "cshow";
	//annars ändra till class = hide
	else
		document.getElementById("cshowhide").className = "chide";
}

//Ger "hidebutton" alltså knappen man trycker på för att toggla ett namn
var chidebutton = document.querySelector("#chidebutton");
//Gör att när man trycker på showhide knappen så kör man toggle funktionen
chidebutton.onclick = ctoggle

